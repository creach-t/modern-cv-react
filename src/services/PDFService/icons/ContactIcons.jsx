/**
 * Sélectionne l'icône appropriée selon le type
 * @param {string} type - Type d'icône
 * @param {Object} props - Propriétés à passer à l'icône
 * @returns {React.ReactElement|null} - Composant d'icône ou null si type non reconnu
 */
export const getContactIcon = (type, props = {}) => {
  const IconComponent = contactIconComponents[type.toLowerCase()];
  if (!IconComponent) return null;
  
  return <IconComponent {...props} />;
};

